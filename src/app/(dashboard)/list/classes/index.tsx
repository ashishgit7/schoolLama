'use client'
import FormModal from "@/components/FormModal";
import Pagination from "@/components/Pagination";
import Table from "@/components/Table";
import TableSearch from "@/components/TableSearch";
import { classesData, role } from "@/lib/data";
import Image from "next/image";
import { useEffect, useState } from "react";
import axios from "axios";
import AppLoader from "@/components/AppLoader";
const baseUrl = process.env.NEXT_PUBLIC_BASE_URL;

type Class = {
  id: number;
  name: string;
  capacity: number;
  grade: number;
  supervisor: string;
};

const columns = [
  {
    header: "Class Name",
    accessor: "name",
  },
  {
    header: "Capacity",
    accessor: "capacity",
    className: "hidden md:table-cell",
  },
  {
    header: "Section",
    accessor: "Section",
    className: "hidden md:table-cell",
  },
  {
    header: "Supervisor",
    accessor: "supervisor",
    className: "hidden md:table-cell",
  },
  {
    header: "Actions",
    accessor: "action",
  },
];

const ClassListPage = () => {

  const [classesInfo, setClassesInfo] = useState<Class[]>([]);
  const [loading, setLoading] = useState(true);
  useEffect(() => {
    const fetchClasses = async () => {
      const res = await axios.get(`${baseUrl}/api/classes`);
      console.log(res.data.documents);
      setClassesInfo(res.data.documents);
      setLoading(false);
    };

    fetchClasses();
    
  }, []);
  const renderRow = (item: any) => (
    <tr
      key={item.$id}
      className="border-b border-gray-200 even:bg-slate-50 text-sm hover:bg-lamaPurpleLight"
    >
      <td className="flex items-center gap-4 p-4">{item.Standard} {item.Section}</td>
      <td className="hidden md:table-cell">50</td>
      <td className="hidden md:table-cell">{item.Section}</td>
      <td className="hidden md:table-cell">{item.Supervisor}</td>
      <td>
        <div className="flex items-center gap-2">
          {role === "admin" && (
            <>
              <FormModal table="class" type="update" data={item} />
              <FormModal table="class" type="delete" id={item.id} />
            </>
          )}
        </div>
      </td>
    </tr>
  );

  return loading ? <AppLoader /> : (
    <div className="bg-white p-4 rounded-md flex-1 m-4 mt-0">
      {/* TOP */}
      <div className="flex items-center justify-between">
        <h1 className="hidden md:block text-lg font-semibold">All Classes</h1>
        <div className="flex flex-col md:flex-row items-center gap-4 w-full md:w-auto">
          <TableSearch />
          <div className="flex items-center gap-4 self-end">
            <button className="w-8 h-8 flex items-center justify-center rounded-full bg-lamaYellow">
              <Image src="/filter.png" alt="" width={14} height={14} />
            </button>
            <button className="w-8 h-8 flex items-center justify-center rounded-full bg-lamaYellow">
              <Image src="/sort.png" alt="" width={14} height={14} />
            </button>
            {role === "admin" && <FormModal table="class" type="create" />}
          </div>
        </div>
      </div>
      {/* LIST */}
      <Table columns={columns} renderRow={renderRow} data={classesInfo} />
      {/* PAGINATION */}
      <Pagination />
    </div>
  );
};

export default ClassListPage;